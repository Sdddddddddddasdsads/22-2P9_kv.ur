"""Модуль для решения квадратного уравнения."""


def kv_ur(a, b, c):
    """Решает квадратное или линейное уравнение."""
    if a == 0:
        if b == 0:
            if c == 0:
                return "Прямая совпадает с осью Ox", "R"
            return "Прямая параллельна оси Ox", "Корней нет"
        x = -c / b
        return "Линейное уравнение, один корень", x

    disc = b ** 2 - 4 * a * c
    if disc > 0:
        x1 = (-b - disc ** 0.5) / (2 * a)
        x2 = (-b + disc ** 0.5) / (2 * a)
        return "Два корня", x1, x2
    if disc == 0:
        x = -b / (2 * a)
        return "Один корень", x
    return "Комплексные корни", "Нет вещественных решений"

    if a == 0 and b == 0 and c != 0:
        return ('Линейное уравнение. Прямая параллельна оси Ох. '
                'Уравнение не имеет корней.', 'Нет корней')
    if a == 0 and b != 0:
        x = -c/b
        return ('Линейное уравнение. Прямая пересекает ось Ox. '
                'Уравнение имеет один корень.', x)
    d = b**2-4*a*c
    if d > 0:
        x1 = (-b - d**0.5)/2*a
        x2 = (-b + d**0.5)/2*a
        return ('Квадратное уравнение. Дискриминант больше 0. '
                'Уравнение имеет два корня.', d, x1, x2)
    if d == 0:
        x = -b/(2*a)
        return ("Квадратное уравнвение. Дискриминант равен 0. "
                "Уравнение имеет один корень.", d, x)
    return ("Ошибка",)
